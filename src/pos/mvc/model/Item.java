package pos.mvc.model;

/**
 *
 * @author User
 */
public class Item {

    private int id;
    private String name;
    private int qoh;
    private int unitPrice;

    public Item() {
    }

    public Item(int id, String name, int qoh, int unitPrice) {
        this.id = id;
        this.name = name;
        this.qoh = qoh;
        this.unitPrice = unitPrice;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the qoh
     */
    public int getQoh() {
        return qoh;
    }

    /**
     * @param qoh the qoh to set
     */
    public void setQoh(int qoh) {
        this.qoh = qoh;
    }

    /**
     * @return the unitPrice
     */
    public int getUnitPrice() {
        return unitPrice;
    }

    /**
     * @param unitPrice the unitPrice to set
     */
    public void setUnitPrice(int unitPrice) {
        this.unitPrice = unitPrice;
    }

    @Override
    public String toString() {
        return "Item{" + "id=" + id + ", name=" + name + ", qoh=" + qoh + ", unitPrice=" + unitPrice + '}';
    }

    
}
